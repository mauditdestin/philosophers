int	ft_atoi(char *str)
{
	int		sign;
	long	num;

	sign = 1;
	if (*str == '+')
		str++;
	else if (*str == '-')
	{
		sign = -1;
		str++;
	}
	num = 0;
	while (*str)
		num = num * 10 + (*str++ - '0');
	return ((int)(sign * num));
}
